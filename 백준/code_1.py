# 문제 설명:
# 배열 arr가 주어집니다. 배열 arr의 각 원소는 숫자 0부터 9까지로 이루어져 있습니다. 
# 이때, 배열 arr에서 연속적으로 나타나는 숫자는 하나만 남기고 전부 제거하려고 합니다.
# 단, 제거된 후 남은 수들의 순서는 유지해야 합니다.

# 예를 들면,
# arr = [1, 1, 3, 3, 0, 1, 1] 이면 [1, 3, 0, 1]을 return 합니다.
# arr = [4, 4, 4, 3, 3] 이면 [4, 3]을 return 합니다.

# 배열 arr에서 연속적으로 나타나는 숫자를 제거하고 남은 수들을 return 하는 solution 함수를 완성해 주세요.

# 제한사항:
# - 배열 arr의 크기: 1,000,000 이하의 자연수
# - 배열 arr의 원소의 크기: 0보다 크거나 같고 9보다 작거나 같은 정수

def solution(arr):
    # 결과를 저장할 리스트 선언 (첫 번째 원소를 넣고 시작)
    answer = [arr[0]]
    
    # 두 번째 원소부터 마지막 원소까지 반복
    for i in range(1, len(arr)):
        # 현재 원소가 직전 원소와 다를 때만 추가
        if arr[i] != arr[i-1]:
            answer.append(arr[i])
    
    # 연속된 숫자가 제거된 결과 리스트 반환
    return answer

# 테스트 예시:
print(solution([1, 1, 3, 3, 0, 1, 1]))  # 출력: [1, 3, 0, 1]
print(solution([4, 4, 4, 3, 3]))        # 출력: [4, 3]
